#include <iostream>
using namespace std;

int main() {
	cout << sizeof (int) <<endl;      //4 bytes = 32 bits
	cout << sizeof (float) <<endl;	  //4 bytes = 32 bits
	cout << sizeof (double) <<endl; 	//8 bytes = 64 bits
	cout << sizeof (char) <<endl;     //1 bytes = 8 bits
	cout << sizeof (bool) <<endl;    //1 bytes = 8 bits
	return 0;
}